import 'dart:math';

enum CoolingZoneType {
  coastal,
  central,
  interior,
  highland,
}

enum CoolingRoomType {
  kitchen,
  hall,
  livingRoom,
  salon,
  childRoom,
  bedRoom,
}

class CoolingDistrictData {
  final String name;
  final CoolingZoneType zone;

  const CoolingDistrictData({
    required this.name,
    required this.zone,
  });
}

class CoolingProvinceData {
  final String name;
  final bool isMetropolitan;
  final bool hasCoastalZone;
  final List<CoolingZoneType> availableZones;
  final List<CoolingDistrictData> districts;

  const CoolingProvinceData({
    required this.name,
    required this.isMetropolitan,
    required this.hasCoastalZone,
    required this.availableZones,
    this.districts = const [],
  });
}

class CoolingCalculationResult {
  final int rawBtu;
  final int recommendedBtu;
  final double zoneCoefficient;
  final double roomMultiplier;
  final int extraPeopleLoadBtu;

  const CoolingCalculationResult({
    required this.rawBtu,
    required this.recommendedBtu,
    required this.zoneCoefficient,
    required this.roomMultiplier,
    required this.extraPeopleLoadBtu,
  });
}

class CoolingData {
  static const List<int> standardBtuCapacities = [
    9000,
    12000,
    18000,
    24000,
    30000,
    36000,
    48000,
  ];

  static const Map<CoolingZoneType, double> zoneCoefficients = {
    CoolingZoneType.highland: 380,
    CoolingZoneType.interior: 430,
    CoolingZoneType.central: 450,
    CoolingZoneType.coastal: 500,
  };

  static const Map<CoolingRoomType, double> roomMultipliers = {
    CoolingRoomType.hall: 0.90,
    CoolingRoomType.bedRoom: 0.95,
    CoolingRoomType.childRoom: 1.00,
    CoolingRoomType.livingRoom: 1.05,
    CoolingRoomType.salon: 1.10,
    CoolingRoomType.kitchen: 1.20,
  };

  static String zoneLabel(CoolingZoneType zone) {
    switch (zone) {
      case CoolingZoneType.coastal:
        return 'Kıyı Kesim';
      case CoolingZoneType.central:
        return 'Merkez';
      case CoolingZoneType.interior:
        return 'Ara Bölge';
      case CoolingZoneType.highland:
        return 'Yüksek Kesim';
    }
  }

  static String roomLabel(CoolingRoomType room) {
    switch (room) {
      case CoolingRoomType.kitchen:
        return 'Mutfak';
      case CoolingRoomType.hall:
        return 'Hol';
      case CoolingRoomType.livingRoom:
        return 'Oturma Odası';
      case CoolingRoomType.salon:
        return 'Salon';
      case CoolingRoomType.childRoom:
        return 'Çocuk Odası';
      case CoolingRoomType.bedRoom:
        return 'Yatak Odası';
    }
  }

  static int ceilToStandardBtu(int rawBtu) {
    for (final value in standardBtuCapacities) {
      if (rawBtu <= value) return value;
    }
    return standardBtuCapacities.last;
  }

  static CoolingCalculationResult calculate({
    required double area,
    required CoolingZoneType zone,
    required CoolingRoomType room,
    required int peopleCount,
  }) {
    final zoneCoefficient = zoneCoefficients[zone]!;
    final roomMultiplier = roomMultipliers[room]!;
    final extraPeople = max(0, peopleCount);
    final extraPeopleLoad = extraPeople * 2050;

    final raw = ((area * zoneCoefficient * roomMultiplier) + extraPeopleLoad).round();
    final recommended = ceilToStandardBtu(raw);

    return CoolingCalculationResult(
      rawBtu: raw,
      recommendedBtu: recommended,
      zoneCoefficient: zoneCoefficient,
      roomMultiplier: roomMultiplier,
      extraPeopleLoadBtu: extraPeopleLoad,
    );
  }

