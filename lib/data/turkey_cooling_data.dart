  static List<CoolingProvinceData> get sortedProvinces {
    final list = [...provinces];
    list.sort((a, b) => a.name.compareTo(b.name));
    return list;
  }

  static CoolingProvinceData provinceByName(String name) {
    return provinces.firstWhere((p) => p.name == name);
  }

  static List<CoolingDistrictData> districtsOf(String provinceName) {
    return provinceByName(provinceName).districts;
  }

  static List<CoolingZoneType> zonesOf(String provinceName) {
    return provinceByName(provinceName).availableZones;
  }

  static bool isMetropolitan(String provinceName) {
    return provinceByName(provinceName).isMetropolitan;
  }
